/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {

	int sub = y + ~x + 1;
	int sub_sign = sub>>31&1;
	int x_sign = x>>31&1;
	int y_sign = y>>31&1;
	//overflow는 x와 y의 부호가 서로 다를 때에만 확인하면 된다.
	//같을 때에는overflow가 발생하지 않기 때문이다.
	
	return (!((x_sign)^(y_sign)) & !(sub_sign)) | 
			//x와 y의 부호가 서로 같고 y-x의 부호가 양수일 때(>=0이고, overflow는 발생 안함)			
			((x_sign & 1) & (!y_sign & 1));
			//x의 부호가 1이고 y의 부호가 0일 때 (양수 > 음수이므로, 무조건 참이다)

}
/* 
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateLeft(int x, int n) {
  int s = (x>> (32 + (~n+1)) ) &   ~( ~0 << n);
  return x<<n | s;
}
