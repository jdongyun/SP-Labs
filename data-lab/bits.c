/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
	int x55555555 = (0x55<<24)+(0x55<<16)+(0x55<<8) + 0x55;
	int x33333333 = (0x33<<24)+(0x33<<16)+(0x33<<8) + 0x33;
	int x0f0f0f0f = (0x0f<<24)+(0x0f<<16)+(0x0f<<8) + 0x0f;
	int x00ff00ff = (0xff<<16)+0xff;
	int x0000ffff = (0xff<<8) + 0xff;

	int result = (x & x55555555/*01010101*/) + (x>>1 & x55555555);
	result = (result & x33333333/*00110011*/) + (result >> 2 & x33333333);
	result = (result + (result >> 4)) & x0f0f0f0f;
	result = (result + (result >> 8)) & x00ff00ff;
	result = (result + (result >> 16)) & x0000ffff;

	return result;
}
